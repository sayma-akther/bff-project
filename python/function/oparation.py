#Added 2 number using function

def sum_2_number(first, second):
    print(f"Sum of {first} + {second} = {first + second}")


#Subtract 2 number using function
def sub_2_num(a, b):
    print(f"Subtraction of {a} - {b} = {a - b}")


#Multiplecation 4 number using function
def multi_4_num(p, q, r, s):
    print(f"Multiplication of {p} * {q} * {r} * {s} = {p* q* r* s}")  


#Division 2 number using function
def divied_2_num(x, y):
    print(f"Division of {x} / {y} = {x / y}")  




sum_2_number(10, 15)
sub_2_num(100, 60)
multi_4_num(2, 9, 7, 5)
divied_2_num(100,2)



